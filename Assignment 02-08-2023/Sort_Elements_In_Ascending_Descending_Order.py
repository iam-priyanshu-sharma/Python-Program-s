# Declare a list
my_list = [4, 2, 1, 5, 3]

# Sorting the list in ascending order using sorted() function
ascending_sorted_list = sorted(my_list)

# Sorting the list in descending order using sorted() function with reverse=True
descending_sorted_list = sorted(my_list, reverse=True)

# Print the original list and both sorted lists
print("Original list:", my_list)
print("Ascending sorted list:", ascending_sorted_list)
print("Descending sorted list:", descending_sorted_list)
