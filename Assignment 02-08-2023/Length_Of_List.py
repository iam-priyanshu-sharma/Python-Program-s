# Using len() function

# Declare a list
my_list = [1, 2, 3, 4, 5]

# Find the length of the list using len() function
length = len(my_list)

# Print the length of the list
print("Length of the list:", length)

# Using Loop
# Declare a list
my_list = [1, 2, 3, 4, 5]

# Find the length of the list using a loop
length = 0
for _ in my_list:
    length += 1

# Print the length of the list
print("Length of the list:", length)
