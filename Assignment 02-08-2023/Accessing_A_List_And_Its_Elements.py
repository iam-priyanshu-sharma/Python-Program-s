# Using index
# Declare a list
my_list = [10, 20, 30, 40, 50]

# Accessing individual elements using index
print("First element:", my_list[0])
print("Third element:", my_list[2])
print("Last element:", my_list[-1])

# Using for loop
# Declare a list
my_list = [10, 20, 30, 40, 50]

# Iterating through the list using a for loop
print("Printing all elements:")
for element in my_list:
    print(element)

# Using list slicing
# Declare a list
my_list = [10, 20, 30, 40, 50]

# Accessing a sublist using slicing
sublist = my_list[1:4]
print("Sublist:", sublist)
